// if extendido
// mostrar por consola si un numero es par o impar

let numero = 3;
if (numero % 2 === 0) {
  console.log("Es Par");
} else {
  console.log("Es impar");
}

// cuando el cuerpo del if es una sola línea
// se puede omitir las llaves
// ejmplo

if (numero % 2 === 0) console.log("Es par");
else console.log("Es impar");

// for extendido
// Mostrar por console los numeros del 1 al 3

for (let i = 1; i <= 3; i++) {
  console.log(i);
}

// Cuando el cuerpo del for es una sola línea
// se puede omitir las llaves
// lo mismo que paso en el if
// ejemplo

for (let i = 1; i <= 3; i++) console.log(i);
